int ddr_load_train_code_jtag(u8 num)
{
	
	u32 dest, isize, dsize;
	int err;
	
	if(num == TRAIN_1D)
	{
		isize = 31000;
		dsize = 1624;
	}
	else
	{
		isize = 23092;
		dsize = 1332;
	}

// imem
	dest = 0x4140000;
	

	err = load_train_code(dest, isize, (u32 *)D1_Imem);
	if(err)
	{
		printf("err num %d\n",err);
	}
	

//dmem
	dest = 0x4150000;
	

	err = load_train_code(dest, dsize, (u32 *)D1_Dmem);
	if(err)
	{
		printf("err num %d\n",err);
	}

	return 0;
}
